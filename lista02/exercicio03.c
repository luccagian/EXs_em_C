/*
 Crie um programa que inicialize as seguintes variáveis:
• lapis = 45
• borrachas = 2345 
• canetas = 420 
• cadernos = 8
• fitas = 13050
A saída deverá ser (tabela):
Lápis         45
Borrachas     2345
Canetas       420
Cadernos      8
Fitas         13050
*/

#include <stdio.h>

int main()
{
    int lapis = 45, borrachas = 2345, canetas = 420, cadernos = 8, fitas = 13050;

    printf("Lápis \t\t\t %d \n", lapis);
    printf("Borrachas \t\t %d \n", borrachas);
    printf("Canetas \t\t %d \n", canetas);
    printf("Cadernos \t\t %d \n", cadernos);
    printf("Fitas \t\t\t %d \n", fitas);

    return 0;

}
